   private void saveUsers() {
      final TerminalUserListModel model = (TerminalUserListModel)usersList.getModel();
      try {
         usersRepository.clearUsers();
         for (TerminalUser user : model.getUsers()) {
            usersRepository.addUser(user);
         }
         usersRepository.save();
      } catch (AuthenticatorException ex) {
         JOptionPane.showMessageDialog(this, ex.getLocalizedMessage(),
                 TerminalUtil.getMessage("UsersWindow.error_saving.msg"), JOptionPane.ERROR_MESSAGE);
         LOG.error("Error saving users to file.", ex);
      } finally {
         btnSaveUsersList.setEnabled(false);
      }
   }

   private void usersListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_usersListValueChanged
      if (! evt.getValueIsAdjusting()) {
         if (usersList.getSelectedIndex() >= 0) {
            final TerminalUser user = ((TerminalUserListModel)usersList.getModel()).getUserAt(usersList.getSelectedIndex());
            if (user != null) {
               String pswd;
               try {
                  pswd = authenticator.decrypt(user.getPassword());
               } catch (AuthenticatorException ex) {
                  JOptionPane.showMessageDialog(this, ex.getLocalizedMessage(),
                          TerminalUtil.getMessage("UsersWindow.error_get_pswd.msg"), JOptionPane.ERROR_MESSAGE);
                  LOG.error("Error decrypting password.", ex);
                  pswd = "";
               }
               username.setText(user.getUsername());
               password.setText(pswd);
               passwordConfirm.setText(pswd);
               chkExpiration.setSelected(user.getExpiration() != null);
               expiration.setEnabled(chkExpiration.isSelected() && ! server.isReadOnly());
               expiration.setDate(user.getExpiration());
               btnRemoveUser.setEnabled(! server.isReadOnly());
               btnConfirmUser.setEnabled(false);
            }
         } else {
            btnRemoveUser.setEnabled(false);
         }
      }
   }//GEN-LAST:event_usersListValueChanged

