   private void btnConfirmUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmUserActionPerformed
      if (isValuesFieldsValid()) {
         final TerminalUserListModel model = (TerminalUserListModel)usersList.getModel();
         TerminalUser user = model.findUser(username.getText().trim());
         try {
            if (user == null) {
               user = new TerminalUser(username.getText().trim(), authenticator.encrypt(new String(password.getPassword())));
               model.addUser(user);
            } else {
               user.setPassword(authenticator.encrypt(new String(password.getPassword())));
            }
         } catch (AuthenticatorException ex) {
            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage(),
                    TerminalUtil.getMessage("UsersWindow.error_setting_user.msg"), JOptionPane.ERROR_MESSAGE);
            LOG.error("Error setting user data.", ex);
            return;
         }
         if (chkExpiration.isSelected()) {
            user.setExpiration(expiration.getDate());
         } else {
            user.setExpiration(null);
         }
         clearFields();
         btnSaveUsersList.setEnabled(! server.isReadOnly());
         username.requestFocus();
         btnConfirmUser.setEnabled(false);
      }
   }//GEN-LAST:event_btnConfirmUserActionPerformed

   private boolean isValuesFieldsValid() throws HeadlessException {
      if (username.getText().trim().isEmpty()) {
         JOptionPane.showMessageDialog(this, TerminalUtil.getMessage("UsersWindow.username_mandatory.msg"),
                 TerminalUtil.getMessage("UsersWindow.username_invalid.msg"), JOptionPane.ERROR_MESSAGE);
         return false;
      }
      if (password.getPassword().length == 0) {
         JOptionPane.showMessageDialog(this, TerminalUtil.getMessage("UsersWindow.pswd_mandatory.msg"),
                 TerminalUtil.getMessage("UsersWindow.pswd_invalid.msg"), JOptionPane.ERROR_MESSAGE);
         return false;
      }
      if (! Arrays.equals(password.getPassword(), passwordConfirm.getPassword())) {
         JOptionPane.showMessageDialog(this, TerminalUtil.getMessage("UsersWindow.pswd_must_match.msg"),
                 TerminalUtil.getMessage("UsersWindow.pswd_invalid.msg"), JOptionPane.ERROR_MESSAGE);
         return false;
      }
      if (chkExpiration.isSelected()) {
         if (! expiration.isEditValid()) {
            JOptionPane.showMessageDialog(this, TerminalUtil.getMessage("UsersWindow.date_expiration_invalid.msg"),
                    TerminalUtil.getMessage("UsersWindow.date_invalid.msg"), JOptionPane.ERROR_MESSAGE);
            return false;
         }
         if (Calendar.getInstance().getTime().compareTo(expiration.getDate()) >= 0) {
            JOptionPane.showMessageDialog(this, TerminalUtil.getMessage("UsersWindow.expiration_in_past.msg"),
                    TerminalUtil.getMessage("UsersWindow.date_invalid.msg"), JOptionPane.ERROR_MESSAGE);
            return false;
         }
      }
      return true;
   }

