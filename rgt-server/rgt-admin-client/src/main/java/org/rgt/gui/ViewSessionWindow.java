   private class UpdateScreenTask extends Thread {

      private final int interval;

      private boolean running = false;

      public UpdateScreenTask(final int interval, final String name) {
         super(name);
         this.interval = interval;
      }

      @Override
      public void run() {
         running = true;
         do {
            try {
               updateScreen();
               sleep(interval);
            } catch (InterruptedException ex) {
               running = false;
            }
         } while (running);
      }

      public void cancel() {
         running = false;
         interrupt();
      }
   }
