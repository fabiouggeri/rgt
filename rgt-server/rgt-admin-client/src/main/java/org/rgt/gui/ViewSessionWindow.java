   private void createTimer() {
      int selectedInterval;
      cancelTimer();
      selectedInterval = comboInterval.getSelectedIndex();
      if (selectedInterval > 0 && selectedInterval < REFRESH_INTERVALS.length) {
         final int interval = REFRESH_INTERVALS[selectedInterval] * 1000;
         screenUpdater = new UpdateScreenTask(interval, "Monitoring Session: " + session);
         screenUpdater.start();
      }
      btnRefresh.setEnabled(selectedInterval <= 0);
   }

   private void cancelTimer() {
      if (screenUpdater != null) {
         screenUpdater.cancel();
         screenUpdater = null;
      }
      btnRefresh.setEnabled(true);
   }

   private void resizeWindow() {
      final Insets insets = getInsets();
      final int width = insets.left + insets.right + terminalScreen.getPreferredSize().width;
      final int height = insets.top + insets.bottom + topPanel.getPreferredSize().height + terminalScreen.getPreferredSize().height;
      setSize(new Dimension(width, height));
   }

   private void exit() {
      if (sessionsInView.containsKey(session)) {
         sessionsInView.remove(session);
         cancelTimer();
         executor.clear();
         java.awt.EventQueue.invokeLater(() -> setVisible(false));
      }
   }

