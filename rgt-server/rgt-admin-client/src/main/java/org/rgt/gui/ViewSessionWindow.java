   private void configureComponents() {
      setIntervalOptions();
      fontSize.setModel(new DefaultComboBoxModel<>(FONT_SIZE_OPTIONS));
      fontSize.setSelectedIndex(2);
   }

   private void setIntervalOptions() {
      comboInterval.addItem(TerminalUtil.getMessage("ViewSessionWindow.interval.manualOption"));
      /* First entry is manual option */
      for (int i = 1; i < REFRESH_INTERVALS.length; i++) {
         comboInterval.addItem(TerminalUtil.getMessage("ViewSessionWindow.interval.secondsOption", REFRESH_INTERVALS[i]));
      }
   }

   private void updateScreen() {
      try {
         final TextScreen screen = server.getTerminalScreen(session.getId());
         if (screen == null) {
            cancelTimer();
            return;
         }
         if (terminalScreen.getColumns() != screen.getColumns() || terminalScreen.getRows() != screen.getRows()) {
            terminalScreen.setRowsColumns(screen.getRows(), screen.getColumns());
            resizeWindow();
         }
         for (int r = 0; r < screen.getRows(); r++) {
            for (int c = 0; c < screen.getColumns(); c++) {
               terminalScreen.print(r, c, screen.getChar(r, c), screen.getFGColor(r, c), screen.getBGColor(r, c));
            }
         }
         terminalScreen.repaint();
      } catch (TerminalException ex) {
         JOptionPane.showMessageDialog(this,
                 TerminalUtil.getMessage("ViewSessionsWindow.update_eror.msg") + ":\n" + ex.getMessage(),
                 TerminalUtil.getMessage("ViewSessionsWindow.update_eror.title"),
                 JOptionPane.ERROR_MESSAGE);
         exit();
      }
   }


