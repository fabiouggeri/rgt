   private void configureSessionsTable() {
      TableColumnModel colModel = sessionsTable.getColumnModel();
      colModel.getColumn(SESSION_ID).setResizable(true);
      colModel.getColumn(SESSION_TE_ADDRESS).setResizable(true);
      colModel.getColumn(SESSION_USER).setResizable(true);
      colModel.getColumn(SESSION_PID).setResizable(true);
      colModel.getColumn(SESSION_STATUS).setResizable(true);
      colModel.getColumn(SESSION_LOGON_TIME).setResizable(true);
      colModel.getColumn(SESSION_CMD_LINE).setResizable(true);
      colModel.getColumn(SESSION_ID).setPreferredWidth(30);
      colModel.getColumn(SESSION_TE_ADDRESS).setPreferredWidth(90);
      colModel.getColumn(SESSION_USER).setPreferredWidth(100);
      colModel.getColumn(SESSION_PID).setPreferredWidth(50);
      colModel.getColumn(SESSION_STATUS).setPreferredWidth(40);
      colModel.getColumn(SESSION_LOGON_TIME).setPreferredWidth(120);
      colModel.getColumn(SESSION_CMD_LINE).setPreferredWidth(450);
      sessionsTable.setColumnSelectionAllowed(false);
      sessionsTable.setRowSelectionAllowed(true);
      sessionsTable.getSelectionModel().addListSelectionListener(new SessionSelectionListener());
      TableFilter.forTable(sessionsTable).apply().repaint();
   }

   private class SessionSelectionListener implements ListSelectionListener {

      @Override
      public void valueChanged(ListSelectionEvent e) {
         if (!e.getValueIsAdjusting()) {
            selectedCount.setText(Integer.toString(sessionsTable.getSelectedRowCount()));
            btnKill.setEnabled(! server.isReadOnly() && sessionsTable.getSelectedRowCount() > 0);
            btnViewScreen.setEnabled(sessionsTable.getSelectedRowCount() > 0);
         }
      }

   }
