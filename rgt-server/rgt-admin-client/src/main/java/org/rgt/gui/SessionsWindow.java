   private void killSelectedSessions() throws HeadlessException {
      final int[] selectedRows = sessionsTable.getSelectedRows();
      if (JOptionPane.showConfirmDialog(null, TerminalUtil.getMessage("SessionsWindow.confirm_kill.msg", selectedRows.length),
              TerminalUtil.getMessage("SessionsWindow.kill_sessions.msg"), JOptionPane.YES_NO_OPTION,
              JOptionPane.QUESTION_MESSAGE) == 0) {
         try {
            killSessions(selectedRows);
         } catch (TerminalException ex) {
            LOG.error("Error killing session", ex);
            JOptionPane.showMessageDialog(null, TerminalUtil.getMessage("SessionsWindow.error_kill_sessions.msg") + ex.getMessage(),
                    TerminalUtil.getMessage("SessionsWindow.server_error.msg"), JOptionPane.ERROR_MESSAGE);
            if (!server.isEmbedded() && !server.isConnected()) {
               exit();
            }
         }
      }
   }

   private void killSessions(final int[] selectedRows) throws TerminalException, HeadlessException {
      final SessionTableModel model = (SessionTableModel) sessionsTable.getModel();
      final long[] selectedIds = new long[selectedRows.length];
      int killCount = 0;
      for (int i = 0; i < selectedRows.length; i++) {
         final Session s = model.getSession(sessionsTable.convertRowIndexToModel(selectedRows[i]));
         selectedIds[i] = s != null ? s.getId() : 0L;
      }
      for (long id : selectedIds) {
         if (id > 0) {
            if (server.killSession(id)) {
               ++killCount;
               model.removeSession(id);
            }
         }
      }
      sessionsCount.setText(Integer.toString(server.getSessionsCount()));
      JOptionPane.showMessageDialog(null, TerminalUtil.getMessage("SessionsWindow.sessions_killed.msg", killCount),
              TerminalUtil.getMessage("SessionsWindow.killed_sessions_title.msg"), JOptionPane.INFORMATION_MESSAGE);
   }

   private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
      executor.execute(() -> {
         try {
            loadSessions();
            sessionsTable.requestFocus();
         } catch (TerminalException ex) {
            LOG.error("Error getting sessions", ex);
            JOptionPane.showMessageDialog(null, TerminalUtil.getMessage("SessionsWindow.error_get_sessions.msg") +
                    ex.getMessage(), TerminalUtil.getMessage("SessionsWindow.server_error.msg"),
                    JOptionPane.ERROR_MESSAGE);
            if (! server.isEmbedded() && ! server.isConnected()) {
               exit();
            }
         }
      });
   }//GEN-LAST:event_btnRefreshActionPerformed

   private void loadSessions() throws TerminalException {
      final SessionTableModel model = (SessionTableModel) sessionsTable.getModel();
      model.clearSessions(false);
      model.addSessions((Collection<Session>)server.getSessions(), false);
      model.fireTableDataChanged();
      sessionsCount.setText(Integer.toString(server.getSessionsCount()));
   }

