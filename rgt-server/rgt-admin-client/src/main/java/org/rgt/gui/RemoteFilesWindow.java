   private void configureFilesTable() {
      final TableColumnModel colModel = filesTable.getColumnModel();
      final DecoratorTableCellRenderer fileSizeRenderer;
      final DecoratorTableCellRenderer dateTimeRenderer;
      final EditableTableRowSorter rowSorter;

      fileSizeRenderer = new DecoratorTableCellRenderer((TableCellRendererDecorator<FileInfoModel, JLabel>) (table, model, label, row, col, sel, focus) -> {
         final int modelRow = filesTable.convertRowIndexToModel(row);
         final FileInfo file = modelRow >= 0 ? model.fileInfoAt(modelRow) : null;
         if (file == null || file.isDirectory()) {
            label.setText("");
         } else if (file.getLength() == 0L) {
            label.setText("0 KB");
         } else if (file.getLength() < 1024L) {
            label.setText("1 KB");
         } else {
            label.setText(((long) (file.getLength() / 1024)) + " KB");
         }
      });

      dateTimeRenderer = new DecoratorTableCellRenderer((TableCellRendererDecorator<FileInfoModel, JLabel>) (table, model, label, row, col, sel, focus) -> {
         final FileInfo file = model.fileInfoAt(filesTable.convertRowIndexToModel(row));
         final Date dateTime;
         if (file == null) {
            return;
         }
         switch (col) {
            case FILE_CREATION:
               dateTime = file.getCreationTime();
               break;
            case FILE_MODIFIED:
               dateTime = file.getLastModificationTime();
               break;
            default:
               return;
         }
         label.setText(DATE_FORMAT.format(dateTime));
      });

      filesTable.setModel(new FileInfoModel());
      filesTable.getTableHeader().setToolTipText(TerminalUtil.getMessage("RemoteFilesWindow.files.header.tooltip"));
      colModel.getColumn(FILE_NAME).setResizable(true);
      colModel.getColumn(FILE_NAME).setCellRenderer(new DecoratorTableCellRenderer(new FileExtensionDecorator()));
      colModel.getColumn(FILE_NAME).setPreferredWidth(250);
      colModel.getColumn(FILE_SIZE).setResizable(true);
      colModel.getColumn(FILE_SIZE).setPreferredWidth(70);
      colModel.getColumn(FILE_SIZE).setCellRenderer(fileSizeRenderer);
      colModel.getColumn(FILE_CREATION).setResizable(true);
      colModel.getColumn(FILE_CREATION).setPreferredWidth(100);
      colModel.getColumn(FILE_CREATION).setCellRenderer(dateTimeRenderer);
      colModel.getColumn(FILE_MODIFIED).setResizable(true);
      colModel.getColumn(FILE_MODIFIED).setPreferredWidth(100);
      colModel.getColumn(FILE_MODIFIED).setCellRenderer(dateTimeRenderer);
      filesTable.setColumnSelectionAllowed(false);
      filesTable.setRowSelectionAllowed(true);
      filesTable.getSelectionModel().addListSelectionListener(this);
      TableFilter.forTable(filesTable).apply().repaint();
      rowSorter = (EditableTableRowSorter) filesTable.getRowSorter();
      rowSorter.setRowComparator(fileSortComparator);
      rowSorter.setRowFilter(new MultiRowFilter<>(rowSorter.getRowFilter(), extensionRowFilter));
   }

   private void sortFilesModel(final FileInfoModel filesModel) {
      final List<RowSorter.SortKey> sortKeys = ((EditableTableRowSorter) filesTable.getRowSorter()).getSortKeys();
      if (sortKeys.isEmpty()) {
         fileSortComparator.sort(filesModel, 0, SortOrder.ASCENDING);
      } else {
         fileSortComparator.sort(filesModel, sortKeys);
      }
   }

   private void updateModel(final RemoteFolder remoteFolder) {
      final FileInfoModel filesModel = filesModel();
      filesModel.clearFiles(false);
      filesModel.addFiles(remoteFolder.files(), false);
      sortFilesModel(filesModel);
      filesModel.fireTableDataChanged();
      if (filesModel.getRowCount() > 0) {
         filesTable.setRowSelectionInterval(0, 0);
      }
   }

   private void loadFiles(final String path, boolean notify) {
      try {
         final RemoteFolder remoteFolder;
         loadingRemoteFiles = true;
         progressBar.setIndeterminate(true);
         progressPanel.setVisible(true);
         remoteFolder = server.listRemoteFiles(path);
         server.setLastViewedRemotePath(remoteFolder.pathName());
         currentPath.setText(server.getLastViewedRemotePath());
         if (notify) {
            notifyListRemoteFiles(remoteFolder.files());
         }
         java.awt.EventQueue.invokeLater(() -> updateModel(remoteFolder));
      } catch (TerminalException ex) {
         JOptionPane.showMessageDialog(this, ex.getLocalizedMessage(),
                 TerminalUtil.getMessage("RemoteFilesWindow.error_loading.msg"), JOptionPane.ERROR_MESSAGE);
      } finally {
         progressPanel.setVisible(false);
         progressBar.setIndeterminate(false);
         loadingRemoteFiles = false;
      }
   }

