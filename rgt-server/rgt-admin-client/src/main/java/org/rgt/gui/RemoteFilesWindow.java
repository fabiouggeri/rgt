   private int totalSize(final File[] files) {
      int totalSize = 0;
      for (File file : files) {
         if (file.isFile()) {
            totalSize += file.length();
         }
      }
      return totalSize;
   }

   private void uploadFiles(final File[] filesToUpload) {
      final FileInfoModel model = filesModel();
      int success = 0;
      try {
         int totalToUpload = totalSize(filesToUpload);
         progressBar.setMaximum(totalToUpload);
         progressBar.setValue(0);
         progressPanel.setVisible(true);
         continueRemoteOperation = true;
         for (File localFile : filesToUpload) {
            final File remoteFile;
            if (!continueRemoteOperation) {
               notification("Operation canceled by user");
               break;
            }
            remoteFile = new File(server.getLastViewedRemotePath(), localFile.getName());
            try {
               if (server.uploadFile(localFile, server.getLastViewedRemotePath(), false,
                       (int chunk) -> {
                          progressBar.setValue(progressBar.getValue() + chunk);
                          return continueRemoteOperation;
                       })) {
                  ++success;
                  notifyUploadRemoteFile(new FileInfo(remoteFile.getPath()), new FileInfo(localFile));
                  model.addFile(new FileInfo(localFile));
               }
            } catch (TerminalException ex) {
               notification(TerminalUtil.getMessage("RemoteFilesWindow.upload_file_error", remoteFile, localFile, server, ex.getMessage()));
            }
         }
      } finally {
         notification(TerminalUtil.getMessage("RemoteFilesWindow.operation_completed.msg", success, filesToUpload.length - success));
         progressPanel.setVisible(false);
      }
   }

