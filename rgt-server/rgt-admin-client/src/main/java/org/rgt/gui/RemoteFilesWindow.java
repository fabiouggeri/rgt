   private void filesTableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_filesTableKeyPressed
      switch (evt.getKeyCode()) {
         case KeyEvent.VK_ENTER:
            enterSelectedFolder();
            break;
         case KeyEvent.VK_DELETE:
            removeRemoteFiles();
            break;
         case KeyEvent.VK_HOME:
            selectFirstRow(evt);
            break;
         case KeyEvent.VK_END:
            selectLastRow(evt);
            break;
         default:
            if (isValidFilenameChar(evt.getKeyChar())) {
               if (Duration.between(lastKeyTyped, Instant.now()).toSeconds() > 2) {
                  currentSearch.setLength(0);
               }
               currentSearch.append(evt.getKeyChar());
               lastKeyTyped = Instant.now();
               searchNextFile();
            } else {
               currentSearch.setLength(0);
            }
            break;
      }
   }//GEN-LAST:event_filesTableKeyPressed

