   private void verifySelection() {
      final FileInfoModel model = filesModel();
      int[] selectedRows = modelSelectedRows();
      int selectedFiles = 0;
      int selectedDirs = 0;
      long totalSize = 0;
      for (int row : selectedRows) {
         final FileInfo file = model.fileInfoAt(row);
         if (file.isFile()) {
            if (file.getLength() < 1024L) {
               ++totalSize;
            } else {
               totalSize += (long) (file.getLength() / 1024);
            }
            ++selectedFiles;
         } else {
            ++selectedDirs;
         }
      }
      btnRemove.setEnabled(selectedFiles > 0 && selectedDirs == 0);
      btnDownload.setEnabled(selectedFiles > 0 && selectedDirs == 0);
      totalSelectedItems.setText(TerminalUtil.getMessage("RemoteFilesWindow.selected_files_status", selectedFiles, totalSize + " KB"));
   }

   private class MultiRowFilter<T extends TableModel> implements ModelRowFilter<T> {

      private final List<ModelRowFilter<T>> filters = new ArrayList<>();

      public MultiRowFilter(ModelRowFilter<T>... filters) {
         this.filters.addAll(Arrays.asList(filters));
      }

      @Override
      public boolean include(T model, int row) {
         for (ModelRowFilter<T> f : filters) {
            if (!f.include(model, row)) {
               return false;
            }
         }
         return true;
      }

      @Override
      public boolean isFiltered(int column) {
         for (ModelRowFilter<T> f : filters) {
            if (f.isFiltered(column)) {
               return true;
            }
         }
         return false;
      }

   }

   private class FileMaskRowFilter implements ModelRowFilter<FileInfoModel> {

      private final List<Pattern> maskPatterns = new ArrayList<>();

      public FileMaskRowFilter(final String... filters) {
         for (final String f : filters) {
            final Pattern p = createFilterPattern(f);
            if (p != null) {
               maskPatterns.add(p);
            }
         }
      }

      @Override
      public boolean include(FileInfoModel model, int row) {
         final FileInfo file;
         if (maskPatterns.isEmpty()) {
            return true;
         }
         file = model.fileInfoAt(row);
         if (file == null) {
            return false;
         }
         if (file.isDirectory()) {
            return true;
         }
         for (Pattern p : maskPatterns) {
            if (p.matcher(file.getName()).matches()) {
               return true;
            }
         }
         return false;
      }

      @Override
      public boolean isFiltered(int column) {
         return !maskPatterns.isEmpty() && column == FILE_NAME;
      }

      public boolean addFilter(final String filter) {
         final Pattern p = createFilterPattern(filter);
         if (p != null) {
            return maskPatterns.add(p);
         }
         return false;
      }

      public void clearFilters() {
         maskPatterns.clear();
      }

      private Pattern createFilterPattern(String filter) {
         if (filter.indexOf('*') >= 0 || filter.indexOf('?') >= 0) {
            final StringBuilder sb = new StringBuilder(filter.length() + 12);
            for (int i = 0; i < filter.length(); i++) {
               final char c = filter.charAt(i);
               switch (c) {
                  case '*':
                     sb.append(".*");
                     break;
                  case '?':
                     sb.append('.');
                     break;
                  case '.':
                     sb.append('\\').append('.');
                     break;
                  default:
                     sb.append(c);
                     break;
               }
            }
            return Pattern.compile(sb.toString());
         }
         return null;
      }

   }

   private class FileInfoSortComparator implements RowComparator<FileInfoModel> {

      public void sort(final FileInfoModel model, final List<RowSorter.SortKey> sortKeys) {
         model.sort((FileInfo file1, FileInfo file2) -> {
            for (RowSorter.SortKey sortKey : sortKeys) {
               final int result = compareFiles(sortKey.getColumn(), file1, file2);
               if (result != 0) {
                  return result * (sortKey.getSortOrder() == SortOrder.ASCENDING ? 1 : -1);
               }
            }
            return 0;
         });
      }

      public void sort(final FileInfoModel model, final int column, final SortOrder order) {
         model.sort((FileInfo f1, FileInfo f2) -> compareFiles(column, f1, f2) * (order == SortOrder.ASCENDING ? 1 : -1));
      }

      @Override
      public int compare(final FileInfoModel model, RowSorter.SortKey sortKey, int firstRow, int secondRow) {
         final FileInfo file1 = model.fileInfoAt(firstRow);
         final FileInfo file2 = model.fileInfoAt(secondRow);
         return compareFiles(sortKey.getColumn(), file1, file2) * (sortKey.getSortOrder() == SortOrder.ASCENDING ? 1 : -1);
      }

      private int compareFiles(int column, FileInfo file1, FileInfo file2) {
         int comp;
         if (file1.getFileType() == file2.getFileType()) {
            switch (column) {
               case 1:
                  comp = Long.signum(file1.getLength() - file2.getLength());
                  break;
               case 2:
                  comp  = file1.getCreationTime().compareTo(file2.getCreationTime());
                  break;
               case 3:
                  comp  = file1.getLastModificationTime().compareTo(file2.getLastModificationTime());
                  break;
               default:
                  comp = file1.getName().compareToIgnoreCase(file2.getName());
                  break;
            }
         } else if (file1.isDirectory()) {
            comp = -1;
         } else {
            comp = 1;
         }
         return comp;
      }

   }
