   private int confirmReplaceLocalFiles(final File localFolder, final FileInfo[] filesToDownload) {
      final List<File> foundFiles = new ArrayList<>();
      for (final FileInfo file : filesToDownload) {
         final File localFile = new File(localFolder, file.getName());
         if (localFile.exists()) {
            foundFiles.add(localFile);
         }
      }
      if (!foundFiles.isEmpty()) {
         return JOptionPane.showConfirmDialog(this, TerminalUtil.getMessage("RemoteFilesWindow.local_replace_confirmation.msg",
                 TerminalUtil.toString(foundFiles, "\n")),
                 TerminalUtil.getMessage("RemoteFilesWindow.local_replace_confirmation.title"),
                 JOptionPane.YES_NO_CANCEL_OPTION);
      }
      return JOptionPane.YES_OPTION;
   }

   private void btnDownloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDownloadActionPerformed
      final JFileChooser fileChooser;
      if (lastLocalSelectedFolder == null) {
         lastLocalSelectedFolder = new File(System.getProperty("user.home"));
      }
      fileChooser = new JFileChooser(lastLocalSelectedFolder);
      fileChooser.setDialogType(JFileChooser.SAVE_DIALOG);
      fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
      if (fileChooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
         final File selectedFile = fileChooser.getSelectedFile();
         if (selectedFile != null && selectedFile.isDirectory()) {
            final FileInfo[] filesToDownload = selectedFiles();
            final int confirm;
            lastLocalSelectedFolder = fileChooser.getSelectedFile();
            confirm = confirmReplaceLocalFiles(fileChooser.getSelectedFile(), filesToDownload);
            if (confirm == JOptionPane.YES_OPTION || confirm == JOptionPane.NO_OPTION) {
               executor.execute(() -> downloadSelectedFiles(fileChooser.getSelectedFile(),
                       filesToDownload, confirm == JOptionPane.YES_OPTION));
            }
         } else {
            JOptionPane.showMessageDialog(this, TerminalUtil.getMessage("RemoteFilesWindow.invalid_local_dir.msg"),
                    TerminalUtil.getMessage("RemoteFilesWindow.invalid_local_dir.title"), JOptionPane.ERROR_MESSAGE);
         }
      }
   }//GEN-LAST:event_btnDownloadActionPerformed

