   private void downloadSelectedFiles(final File localFolder, final FileInfo[] filesToDownload, final boolean overwriteLocal) {
      int success = 0;
      try {
         final int totalToDownload = totalSize(filesToDownload);
         progressBar.setMaximum(totalToDownload);
         progressBar.setValue(0);
         progressPanel.setVisible(true);
         continueRemoteOperation = true;
         for (FileInfo file : filesToDownload) {
            final File remoteFile;
            final File localFile;
            if (!continueRemoteOperation) {
               notification("Operation canceled by user");
               break;
            }

            remoteFile = new File(server.getLastViewedRemotePath(), file.getName());
            localFile = new File(localFolder, file.getName());
            try {
               if (server.downloadFile(remoteFile.getPath(), localFile, overwriteLocal,
                       (int chunk) -> {
                          progressBar.setValue(progressBar.getValue() + chunk);
                          return continueRemoteOperation;
                       })) {
                  ++success;
                  notifyDownloadRemoteFile(new FileInfo(remoteFile.getPath()), new FileInfo(localFile.getParent()));
               }
            } catch (TerminalException ex) {
               notification(TerminalUtil.getMessage("RemoteFilesWindow.download_file_error", remoteFile, localFile, server, ex.getMessage()));
            }
         }
      } finally {
         notification(TerminalUtil.getMessage("RemoteFilesWindow.operation_completed.msg", success, filesToDownload.length - success));
         progressPanel.setVisible(false);
      }
   }

   private void removeSelectedFiles() {
      final FileInfo[] filesToRemove = selectedFiles();
      final FileInfoModel model = filesModel();
      int success = 0;
      try {
         progressBar.setMaximum(filesToRemove.length);
         progressBar.setValue(0);
         progressPanel.setVisible(true);
         continueRemoteOperation = true;
         for (FileInfo file : filesToRemove) {
            final File remoteFile;
            if (!continueRemoteOperation) {
               notification("Operation canceled by user");
               break;
            }
            remoteFile = new File(server.getLastViewedRemotePath(), file.getName());
            try {
               if (server.removeFile(server.getLastViewedRemotePath(), file.getName())) {
                  notifyRemoveRemoteFile(new FileInfo(remoteFile.getPath()));
                  model.removeFile(file);
                  ++success;
               }
            } catch (TerminalException ex) {
               notification(TerminalUtil.getMessage("RemoteFilesWindow.remove_file_error", remoteFile, server, ex.getMessage()));
            } finally {
               progressBar.setValue(progressBar.getValue() + 1);
            }
         }
      } finally {
         notification(TerminalUtil.getMessage("RemoteFilesWindow.operation_completed.msg", success, filesToRemove.length - success));
         progressPanel.setVisible(false);
      }
   }

