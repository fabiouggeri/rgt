   private void deleteRow() {
      final ConfigTableModel model = (ConfigTableModel) configTable.getModel();
      final int selectedCol = configTable.getSelectedColumn();
      final int selectedRow = configTable.getSelectedRow();
      final int modelRow = configTable.convertRowIndexToModel(selectedRow);
      model.delete(modelRow);
      editors.remove(modelRow);
      if (selectedRow < configTable.getRowCount()) {
         configTable.changeSelection(selectedRow, selectedCol, false, false);
      } else if (configTable.getRowCount() > 0) {
         configTable.changeSelection(configTable.getRowCount() - 1, selectedCol, false, false);
      }
   }

   private void configTableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_configTableKeyPressed
      switch (evt.getKeyCode()) {
         case KeyEvent.VK_ESCAPE:
            cancelCellEditing();
            evt.consume();
            break;
         case KeyEvent.VK_ENTER:
            if (editSelectedCell(evt, false)) {
               evt.consume();
            }
            break;
         case KeyEvent.VK_DELETE:
            if (deleteSelectedRow(evt)) {
               evt.consume();
            }
            break;
         case KeyEvent.VK_INSERT:
            insertRow();
            evt.consume();
            break;
         case KeyEvent.VK_PASTE:
            if (editSelectedCell(evt, true)) {
               evt.consume();
            }
            break;
         case KeyEvent.VK_V:
            if (evt.isControlDown() && editSelectedCell(evt, true)) {
               evt.consume();
            }
            break;
         default:
            break;
      }

   }//GEN-LAST:event_configTableKeyPressed

