   private void insertRow() {
      final int tableRow;
      final ConfigTableModel model = (ConfigTableModel) configTable.getModel();
      model.add("");
      editors.add(createTextEditor(""));
      tableRow = configTable.convertRowIndexToView(model.getRowCount() - 1);
      configTable.changeSelection(tableRow, 0, false, false);
      configTable.editCellAt(tableRow, 0);
   }

