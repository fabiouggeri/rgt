   private void stopEditingCell() {
      if (configTable.getCellEditor() != null) {
         configTable.getCellEditor().stopCellEditing();
      }
   }

   private void cancelCellEditing() {
      if (configTable.isEditing() && configTable.getCellEditor() != null) {
         configTable.getCellEditor().cancelCellEditing();
      }
   }

   private void pasteClipboard(Component editor, String text) {
      if (TerminalUtil.isEmpty(text)) {
         return;
      }
      for (int i = 0; i < text.length(); i++) {
         final char c = text.charAt(i);
         editor.dispatchEvent(new KeyEvent(editor, KeyEvent.KEY_TYPED, System.currentTimeMillis(), 0, KeyEvent.VK_UNDEFINED, c));
      }
   }

   private void editCell(final int row, final int col, final boolean pasteClipboard) {
      Component editor;
      configTable.changeSelection(row, col, false, false);
      configTable.editCellAt(row, col);
      editor = configTable.getEditorComponent();
      if (editor != null) {
         if (pasteClipboard) {
            pasteClipboard(editor, TerminalUtil.getTextClipboard());
         }
         editor.requestFocus();
      }
   }

   private void editNextCell() {
      if (configTable.getRowCount() > 0) {
         int row = configTable.getSelectedRow();
         int col = configTable.getSelectedColumn() + 1;
         while (col < configTable.getColumnCount() && !configTable.isCellEditable(row, col)) {
            col++;
         }
         if (col < configTable.getColumnCount()) {
            editCell(row, col, false);
         }
      }
   }

   private boolean editSelectedCell(KeyEvent evt, boolean pasteClipboard) {
      int row;
      int col;
      if (configTable.getRowCount() <= 0) {
         return false;
      }
      row = configTable.getSelectedRow();
      col = configTable.getSelectedColumn();
      if (configTable.isEditing()) {
         if (row == configTable.getEditingRow() && col == configTable.getEditingColumn()) {
            stopEditingCell();
            editNextCell();
            return true;
         }
         stopEditingCell();
      }
      while (col < configTable.getColumnCount() && !configTable.isCellEditable(row, col)) {
         col++;
      }
      if (col >= configTable.getColumnCount()) {
         return false;
      }
      editCell(row, col, pasteClipboard);
      return true;
   }

   private boolean deleteSelectedRow(KeyEvent evt) {
      if (configTable.getRowCount() > 0 && !configTable.isEditing()) {
         deleteRow();
         return true;
      }
      return false;
   }

   private OptionType optionType(TerminalOption<?> terminalOption) {
      if (Boolean.class.isAssignableFrom(terminalOption.getType())) {
         return OptionType.BOOLEAN;
      } else if (Integer.class.isAssignableFrom(terminalOption.getType())) {
         return OptionType.INT;
      } else if (RGTLogLevel.class.isAssignableFrom(terminalOption.getType())) {
         return OptionType.LOG_LEVEL;
      }
      return OptionType.STRING;
   }

   private TextCellEditor createTextEditor(final String optionName) {
      final TextCellEditor editor = new TextCellEditor();
      editor.setTextAreaTitle(optionName);
      editor.addKeyListener(new DefaultCellKeyListener(configTable));
      return editor;
   }

   private CustomCellEditor createEditor(final ConfigOption option) {
      CustomCellEditor editor;
      switch (option.getType()) {
         case BOOLEAN: {
            editor = new ComboCellEditor<>(new String[]{"false", "true"});
            editor.addKeyListener(new DefaultCellKeyListener(configTable, COMBO_KEYS));
            break;
         }
         case INT:
            editor = new IntegerCellEditor(0);
            editor.addKeyListener(new DefaultCellKeyListener(configTable));
            break;
         case LOG_LEVEL: {
            editor = new ComboCellEditor(new String[]{
               RGTLogLevel.OFF.name(),
               RGTLogLevel.ERROR.name(),
               RGTLogLevel.WARNING.name(),
               RGTLogLevel.INFO.name(),
               RGTLogLevel.DEBUG.name(),
               RGTLogLevel.TRACE.name()});
            editor.addKeyListener(new DefaultCellKeyListener(configTable, COMBO_KEYS));
            break;
         }
         default: {
            editor = createTextEditor(option.getName());
            break;
         }
      }
      return editor;
   }

   private ConfigTableModel createModel() {
      final String[] columns;
      final List<String[]> data;
      final Set<ConfigOption> optionsSet = new HashSet<>();
      options.clear();
      for (RemoteTerminalServer server : servers) {
         final Map<String, String> optionsMap = server.getConfiguration().stringValues();
         for (Map.Entry<String, String> option : optionsMap.entrySet()) {
            final ConfigOption configOption = new ConfigOption(option.getKey());
            if (!optionsSet.contains(configOption)) {
               final TerminalOption<?> serverOp = server.getConfiguration().getOption(option.getKey());
               if (serverOp != null) {
                  configOption.setType(optionType(serverOp));
               }
               optionsSet.add(configOption);
            }
         }
      }
      options.addAll(optionsSet);
      Collections.sort(options, (o1, o2) -> o1.getName().compareToIgnoreCase(o2.getName()));
      columns = createColumnsHeaders();
      data = new ArrayList<>(options.size());
      editors.clear();

      for (int row = 0; row < options.size(); row++) {
         final ConfigOption option = options.get(row);
         final String[] rowData = new String[columns.length];
         editors.add(createEditor(option));
         data.add(rowData);
         setDataRow(option, rowData);
      }
      return new ConfigTableModel(columns, data);
   }

   private String[] createColumnsHeaders() {
      final String[] columns = new String[servers.size() + 1];
      columns[0] = TerminalUtil.getMessage("ServerConfigWindow.options.header");
      for (int i = 0; i < servers.size(); i++) {
         columns[i + 1] = servers.get(i).getId();
      }
      return columns;
   }

   private void setDataRow(final ConfigOption option, final String[] rowData) {
      rowData[0] = option.getName();
      for (int col = 1; col <= servers.size(); col++) {
         final RemoteTerminalServer server = servers.get(col - 1);
         String value = null;
         if (option.getName().toLowerCase().startsWith("env.")) {
            value = server.getConfiguration().getEnvVar(option.getName());
         } else {
            final TerminalOption<?> terminalOption = server.getConfiguration().getOption(option.getName());
            if (terminalOption != null) {
               value = terminalOption.text();
            }
         }
         rowData[col] = value != null ? value : "";
      }
   }

   private void configureConfigTable() {
      final TableColumnModel colModel;
      final ImageIcon diffIcon = new ImageIcon(getClass().getResource("/alert_16.png"));
      final ModifiedCellDecorator changeDecorator = new ModifiedCellDecorator().changedBackground(Color.CYAN);
      final ValueDiffDecorator diffDecorator = new ValueDiffDecorator(1).diffIcon(diffIcon).diffBackground(DIFF_CELL_BACKGROUND);
      final DecoratorTableCellRenderer cellRenderer = new DecoratorTableCellRenderer(diffDecorator, changeDecorator);
      final TextCellEditor optionNameEditor = new TextCellEditor();
      final ConfigTableModel model = createModel();

      optionNameEditor.addCellEditorListener(new ValidateDuplicatedKeyListener(configTable, 0,
              v -> JOptionPane.showMessageDialog(null,
                      TerminalUtil.getMessage("ServerConfigWindow.option_already_exists.msg"),
                      TerminalUtil.getMessage("ServerConfigWindow.option_already_exists.title"),
                      JOptionPane.WARNING_MESSAGE),
              true,
              true));
      optionNameEditor.addKeyListener(new DefaultCellKeyListener(configTable));
      configTable.setModel(model);
      configTable.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
      colModel = configTable.getColumnModel();
      colModel.getColumn(OPTION_NAME).setResizable(true);
      colModel.getColumn(OPTION_NAME).setPreferredWidth(calcColumnSize(OPTION_NAME));
      colModel.getColumn(OPTION_NAME).setCellEditor(optionNameEditor);
      colModel.getColumn(OPTION_NAME).setCellRenderer(cellRenderer);
      for (int i = 0; i < servers.size(); i++) {
         colModel.getColumn(i + 1).setResizable(true);
         colModel.getColumn(i + 1).setPreferredWidth(250);
         colModel.getColumn(i + 1).setCellRenderer(cellRenderer);
      }
      configTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
      configTable.setColumnSelectionAllowed(false);
      configTable.setRowSelectionAllowed(false);
      configTable.setCellSelectionEnabled(true);
      configTable.setColumnSelectionInterval(0, 0);
      configTable.setRowSelectionInterval(0, 0);
      copyButtonsSep.setVisible(model.getColumnCount() > 2);
      btnCopyLeft.setVisible(model.getColumnCount() > 2);
      btnCopyRight.setVisible(model.getColumnCount() > 2);
      btnCopyToAll.setVisible(model.getColumnCount() > 2);
      configTable.getModel().addTableModelListener((TableModelEvent e) -> {
         btnSave.setEnabled(model.isChanged());
         btnApply.setEnabled(model.isChanged());
      });
      configTable.getColumnModel().addColumnModelListener(this);
      configTable.getSelectionModel().addListSelectionListener(this);
   }

   private int calcColumnSize(int col) {
      int width = 0;
      for (int row = 0; row < configTable.getRowCount(); row++) {
         TableCellRenderer renderer = configTable.getCellRenderer(row, col);
         Component comp = configTable.prepareRenderer(renderer, row, col);
         width = Math.max(comp.getPreferredSize().width + 6, width);
      }
      return width;
   }

   private void applyConfig() {
      final List<RemoteTerminalServer> serversErrors = new ArrayList<>();
      ConfigTableModel model = (ConfigTableModel) configTable.getModel();
      for (String optionName : model.removed()) {
         servers.forEach(s -> s.getConfiguration().delete(optionName));
      }
      // update options
      for (int row = 0; row < model.getRowCount(); row++) {
         if (model.isChanged(row)) {
            setServerValues(model, row);
         }
      }
      for (RemoteTerminalServer s : servers) {
         try {
            if (!s.setRemoteConfiguration()) {
               serversErrors.add(s);
            }
         } catch (TerminalException ex) {
            serversErrors.add(s);
            message(TerminalUtil.getMessage("ServerConfigWindow.applying_error.msg", s), "\n",
                    ex.getMessage(),
                    "\n================================================================================\n");
         }
      }
      if (!serversErrors.isEmpty()) {
         serversErrors.forEach(s -> reloadConfiguration(s));
         JOptionPane.showMessageDialog(this, TerminalUtil.getMessage("ServerConfigWindow.alert_applying_error.msg"),
                 TerminalUtil.getMessage("ServerConfigWindow.alert_applying_error.title"), JOptionPane.ERROR_MESSAGE);
         configureConfigTable();
      }
      model.clearChanges();
      btnSave.setEnabled(false);
      btnApply.setEnabled(false);
   }

   private void saveConfig() {
      final List<RemoteTerminalServer> serversErrors = new ArrayList<>();
      ConfigTableModel model = (ConfigTableModel) configTable.getModel();
      for (String optionName : model.removed()) {
         servers.forEach(s -> s.getConfiguration().delete(optionName));
      }
      // update options
      for (int row = 0; row < model.getRowCount(); row++) {
         if (model.isChanged(row)) {
            setServerValues(model, row);
         }
      }
      for (RemoteTerminalServer s : servers) {
         try {
            if (!s.saveConfiguration()) {
               serversErrors.add(s);
            }
         } catch (TerminalException ex) {
            serversErrors.add(s);
            message(TerminalUtil.getMessage("ServerConfigWindow.saving_error.msg", s), "\n",
                    ex.getMessage(),
                    "\n================================================================================\n");
         }
      }
      if (!serversErrors.isEmpty()) {
         serversErrors.forEach(s -> reloadConfiguration(s));
         JOptionPane.showMessageDialog(this, TerminalUtil.getMessage("ServerConfigWindow.alert_saving_error.msg"),
                 TerminalUtil.getMessage("ServerConfigWindow.alert_saving_error.title"), JOptionPane.ERROR_MESSAGE);
         configureConfigTable();
      }
      model.clearChanges();
      btnSave.setEnabled(false);
      btnApply.setEnabled(false);
   }

   private void reloadConfiguration(RemoteTerminalServer s) {
      if (s.isConnected()) {
         message(TerminalUtil.getMessage("ServerConfigWindow.reload_config.msg", s), "\n");
         try {
            s.loadConfiguration();
         } catch (TerminalException ex) {
            message(TerminalUtil.getMessage("ServerMainWindow.error_load_config.msg", s), "\n",
                    ex.getMessage(),
                    "\n================================================================================\n");
         }
      }
   }

   private void setServerValues(ConfigTableModel model, int row) {
      final String optionName = model.getOptionName(row);
      if (TerminalUtil.isEmpty(optionName)) {
         return;
      }
      for (int col = 1; col <= servers.size(); col++) {
         if (model.isChanged(row, col)) {
            servers.get(col - 1).getConfiguration().setValue(optionName, model.getValueAt(row, col).toString());
         }
      }
   }

